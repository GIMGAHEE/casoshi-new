// ============================================================
// Grid validation helper
// ============================================================
function grid(rows) {
  if (rows.length !== SPRITE_H) {
    throw new Error(`Grid must have ${SPRITE_H} rows, got ${rows.length}`);
  }
  rows.forEach((r, i) => {
    if (r.length !== SPRITE_W) {
      throw new Error(`Row ${i} width = ${r.length}, expected ${SPRITE_W}: "${r}"`);
    }
  });
  return rows;
}

