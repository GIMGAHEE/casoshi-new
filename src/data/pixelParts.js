// ============================================================
// Token → Color resolver
// ============================================================
export function buildPalette(colors) {
  return {
    '1': colors.skin,
    '2': shade(colors.skin, -0.14),
    '3': colors.hair,
    '4': shade(colors.hair, -0.25),
    '5': EYE,
    '6': colors.outfit,
    '7': shade(colors.outfit, -0.2),
    '8': colors.accent,
    '9': shade(colors.accent, -0.3),
    'W': WHITE,
    'B': BLUSH,
    'M': MOUTH,
    'L': colors.skin,      // bare legs use skin tone
    'l': shade(colors.skin, -0.14),
    'F': SHOE,
    'f': SHOE_SHADOW,
    '_': OUTLINE,
  };
}

// ============================================================
